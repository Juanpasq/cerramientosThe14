								<h2 class="major">Aluminio</h2>
								<span class="image main"><img src="images/aluminio04.jpg" alt="" /></span>
								<h3>Por que aluminio</h3>
                                <p>No necesitan mantenimiento. El aluminio es un material versàtil. Permiten gran variedad de terminaciones. Admiten doble vidriado hermètico.El aluminio protege el medio ambiente. No requieren refuerzos estructurales. Brindan excelente hermeticidad a la infiltraciòn de agua y aire.</p>
								<h3>Linea Modena</h3>
                                <p>Es el Sistema de Carpintería de Aluar, que ha creado un estándar debido a su diseño y al equilibrio peso/resistencia. Su uso extendido lo transforma en el sistema más tradicional entre los carpinteros argentinos. Brinda una gran variedad de combinaciones. Se puede utilizar vidrio simple o Doble Vidriado Hermético (DVH), en las corredizas de aplicación encapsulada y en las batientes con contravidrio de borde recto o curvo.</p>
                                <h3>Linea Herrero</h3>
                                <p>Es un sistema mediano, el más tradicional en el mercado actual de aberturas, con un sistema de cierre central manual. El sistema mediano posee un tipo de perfil que contiene menos espesor que las demás líneas, pero con la misma calidad.</p>
