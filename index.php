                            <article align="justify" id="vidrieria">
								<h2 class="major">Vidrieria</h2>
								<span class="image main"><img src="images/vidrio01.jpg" alt="" /></span>
								<h3>Vidrio DVH</h3>
                                <p>DVH (Doble Vidriado Hermético), un nuevo sistema compuesto por dos vidrios separados entre sí, con una cámara de aire quieto y seco en su interior, herméticamente sellado al paso de la humedad y el vapor de agua. El espesor y el tipo de vidrios a emplear depende de la presión del viento y del tamaño del paño. También es función de los requerimientos de control solar, aislamiento acústico y especificaciones de seguridad y protección.</p>
                                <h3>Vidrio Laminado</h3>
                                <p>Consiste en la unión de varias láminas de vidrio mediante una lámina intermedia. Esta lámina puede ser transparente o de colores. También pueden recibir un tratamiento acústico y de control solar. Esta lámina le confiere al vidrio una seguridad adicional ante roturas, ya que los pedazos quedan unidos a ella. Esta flexibilidad permite hacer de los vidrios laminados un elemento indispensable en la arquitectura y el diseño contemporáneos.</p>
